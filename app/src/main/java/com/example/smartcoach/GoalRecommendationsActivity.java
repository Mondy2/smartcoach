package com.example.smartcoach;

import android.os.Bundle;
import android.widget.Button;
import android.widget.TextView;

import androidx.appcompat.app.AppCompatActivity;

public class GoalRecommendationsActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_goal_recommendations);

        // Отримуємо передану ціль і індекс картки
        String goalType = getIntent().getStringExtra("GOAL_TYPE");
        int cardIndex = getIntent().getIntExtra("CARD_INDEX", 0);

        // Знаходимо елементи інтерфейсу
        TextView goalTitle = findViewById(R.id.goal_title);
        TextView recommendationsText = findViewById(R.id.recommendations_text);
        Button backButton = findViewById(R.id.back_button);

        // Встановлюємо заголовок цілі
        goalTitle.setText(goalType); // Ціль уже перекладена в HomeActivity

        // Встановлюємо рекомендації залежно від цілі та індексу картки
        String recommendations;
        switch (goalType) {
            case "Схуднення":
                if (cardIndex == 0 || cardIndex == 3) { // Перша картка "Схуднення"
                    recommendations = "Для схуднення (варіант 1):\n\n" +
                            "1. **Кардіо тренування**: Виконуйте біг, стрибки на скакалці або їзду на велосипеді 3-4 рази на тиждень по 30-40 хвилин.\n" +
                            "2. **Силові тренування**: Додайте вправи на всі групи м’язів (наприклад, присідання, віджимання, планка) для прискорення метаболізму.\n" +
                            "3. **Дієта**: Скоротіть споживання калорій, їжте більше білка та овочів, уникайте цукру та швидких вуглеводів.\n" +
                            "4. **Режим сну**: Спіть 7-8 годин на добу, щоб уникнути переїдання через стрес.\n" +
                            "5. **Пийте воду**: Вживайте 2-3 літри води на день для підтримання гідратації.";
                } else { // Друга картка "Схуднення"
                    recommendations = "Для схуднення (варіант 2):\n\n" +
                            "1. **Інтервальні тренування**: Спробуйте HIIT (високоінтенсивні інтервальні тренування) 3 рази на тиждень по 20-30 хвилин.\n" +
                            "2. **Функціональні вправи**: Виконуйте комплекси з власною вагою (наприклад, берпі, стрибки, скручування).\n" +
                            "3. **Харчування**: Збільште споживання клітковини (овочі, фрукти), мінімізуйте перекуси.\n" +
                            "4. **Контроль стресу**: Практикуйте медитацію або дихальні вправи для зниження рівня кортизолу.\n" +
                            "5. **Активність**: Намагайтеся проходити 10 000 кроків на день.";
                }
                break;
            case "Набір м’язової маси":
                if (cardIndex == 1 || cardIndex == 4) { // Перша картка "Набір м’язової маси"
                    recommendations = "Для набору м’язової маси (варіант 1):\n\n" +
                            "1. **Силові тренування**: Зосередьтеся на базових вправах (жим лежачи, присідання, станова тяга) 4-5 разів на тиждень.\n" +
                            "2. **Харчування**: Збільште споживання калорій, їжте більше білка (курка, яйця, риба), складних вуглеводів (вівсянка, рис) та здорових жирів (авокадо, горіхи).\n" +
                            "3. **Режим**: Тренуйтеся 45-60 хвилин, робіть 8-12 повторень у 3-4 підходах.\n" +
                            "4. **Відпочинок**: Давайте м’язам відпочинок (48 годин між тренуваннями однієї групи м’язів).\n" +
                            "5. **Добавки**: Розгляньте протеїнові коктейлі або креатин для прискорення відновлення.";
                } else { // Друга картка "Набір м’язової маси"
                    recommendations = "Для набору м’язової маси (варіант 2):\n\n" +
                            "1. **Прогресивне навантаження**: Поступово збільшуйте ваги щотижня, щоб стимулювати ріст м’язів.\n" +
                            "2. **Харчування**: Їжте 5-6 разів на день невеликими порціями, включаючи білкові перекуси (сир, горіхи).\n" +
                            "3. **Ізолюючі вправи**: Додайте вправи на окремі групи м’язів (наприклад, розгинання на трицепс, згинання на біцепс).\n" +
                            "4. **Відновлення**: Використовуйте масаж або розтяжку для зняття напруги в м’язах.\n" +
                            "5. **Сон**: Спіть не менше 8 годин, оскільки ріст м’язів відбувається під час сну.";
                }
                break;
            case "Підтримка форми":
                if (cardIndex == 2 || cardIndex == 5) { // Перша картка "Підтримка форми"
                    recommendations = "Для підтримки форми (варіант 1):\n\n" +
                            "1. **Регулярні тренування**: Виконуйте 3-4 тренування на тиждень, включаючи кардіо та силові вправи.\n" +
                            "2. **Збалансоване харчування**: Їжте помірно, підтримуйте баланс білків, жирів і вуглеводів.\n" +
                            "3. **Функціональні тренування**: Додайте вправи на гнучкість і баланс, такі як йога або пілатес.\n" +
                            "4. **Контроль ваги**: Слідкуйте за калоріями, щоб не набирати зайву вагу.\n" +
                            "5. **Активний спосіб життя**: Більше ходіть пішки, піднімайтеся сходами замість ліфта.";
                } else { // Друга картка "Підтримка форми"
                    recommendations = "Для підтримки форми (варіант 2):\n\n" +
                            "1. **Різноманітність тренувань**: Чергуйте біг, плавання та заняття в тренажерному залі для гармонійного розвитку.\n" +
                            "2. **Харчування**: Додайте суперфуди (наприклад, чіа, шпинат) для підтримання енергії.\n" +
                            "3. **Розтяжка**: Виконуйте розтяжку після кожного тренування для покращення гнучкості.\n" +
                            "4. **Контроль порцій**: Використовуйте метод тарілки (половина — овочі, чверть — білки, чверть — вуглеводи).\n" +
                            "5. **Релакс**: Виділяйте час на відпочинок, щоб уникнути вигорання.";
                }
                break;
            default:
                recommendations = "Виберіть ціль, щоб побачити рекомендації.";
        }
        recommendationsText.setText(recommendations);

        // Обробник натискання на кнопку "Назад"
        backButton.setOnClickListener(v -> finish());
    }
}
